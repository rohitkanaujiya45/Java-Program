class swap
{
    public static void main(String arg[])
    {
        int a=12,b=28,temp;
        System.out.println("Enter the value");
        System.out.println("Before swaping"+a+" "+b);
        temp=a;
        a=b;
        b=temp;
        System.out.println("After swaping"+a+" "+b);
    }
}